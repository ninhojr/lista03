function exibir() {
    document.getElementById('paragrafo').style.display = 'block';
    document.getElementById('esconder').style.display = 'block';
    document.getElementById('exibir').style.display = 'none'
}

function esconder() {
    document.getElementById('paragrafo').style.display = 'none';
    document.getElementById('esconder').style.display = 'none';
    document.getElementById('exibir').style.display = 'block';
}